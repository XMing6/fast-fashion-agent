package service

const OrderSOP = `# 订单问题决策树
1. 订单状态查询
   1.1. 我的订单在哪？ -> 根据订单ID查询订单状态
   1.2. 订单什么时候发货？ -> 返回预计发货时间
2. 订单修改
   2.1. 可以修改/删除订单吗？ -> 检查订单是否仍在处理中
   2.2. 我想给订单添加商品 -> 检查订单是否仍在处理中
   2.3. 修改收货地址 -> 检查订单是否已发货
3. 订单取消
   3.1. 如何取消订单？ -> 检查订单状态
   3.2. 已支付但未发货 -> 协助取消并退款
4. 支付问题
   4.1. 支付失败怎么办？ -> 检查支付状态并提供解决方案
`

const LogisticsSOP = `# 物流问题决策树
1. 包裹超时
   1.1. 超过预计配送时间 >7天无物流更新 -> 提供100%店铺积分或重发选项
   1.2. 超过预计配送时间 3-7天 -> 联系物流公司查询
2. 配送地址
   2.1. 修改配送地址 -> 订单未发货时可更新
   2.2. 地址填写错误 -> 未发货可修改，已发货联系派送员
3. 包裹显示已签收但未收到
   3.1. 首次客户，地址正确 -> 提供重发或100%现金退款
   3.2. 地址可能错误 -> 联系派送员核实
4. 物流跟踪
   4.1. 查询物流进度 -> 提供物流单号和跟踪链接
   4.2. 物流长时间未更新 -> 联系物流公司处理
`
